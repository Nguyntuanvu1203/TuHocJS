//Toán tử gán +=, -=, *=, /=, %=
let m = 10;
m += 5; //Tương đương m = m + 5
console.log(m);

m -= 5; //Tương đương m = m - 5
console.log(m);

m *= 5; //Tương đương m = m * 5
console.log(m);

m /= 5; //Tương đương m = m / 5
console.log(m);

m %= 5; //Tương đương m = m % 5
console.log(m);
