//Toán Tử Logic && - || - !

let a = 10;
// && (VÀ) cả 2 điều kiện đúng thì trả về true và ngược lại
console.log(0 < a && a < 100);
// || (HOẶC) 1 trong 2 điều kiện đúng thì trả về true, chỉ trả về false khi cả 2 điều kiện đều sai
console.log(0 < a || a < 100);
// ! (PHỦ ĐỊNH)
console.log(!(a < 100));
